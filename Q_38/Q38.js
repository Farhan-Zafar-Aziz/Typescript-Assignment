function describe_city(city, city1, city2) {
    console.log(`${city} is in Pakistan.`);
    console.log(`${city1} is in Pakistan.`);
    console.log(`${city2} is in Pakistan.`);
}
describe_city("Karachi", "Lahore", "Islamabad");
// Karachi is in the Pakistan.
// Lahore is in the Pakistan.
// Islamabad is in the Pakistan.
